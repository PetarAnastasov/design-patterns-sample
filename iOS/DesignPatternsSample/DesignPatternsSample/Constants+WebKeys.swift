import Foundation

extension Constants.Web {
    struct Keys {

        static let Businesses = "businesses"
        
        struct Restaurant {
            
            static let Name = "name"
            static let Location = "location"
        }
        
        struct Location {
            
            static let DisplayAddress = "display_address"
        }
    }
}
